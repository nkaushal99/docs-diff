package com.nikhil.service.highlight.model;

public class OldNewDocuments {

  private String oldFile;

  private String oldFileHighlighted;

  private String newFile;

  private String newFileHighlighted;

  public String getOldFile() {
    return oldFile;
  }

  public void setOldFile(String oldFile) {
    this.oldFile = oldFile;
  }

  public String getNewFile() {
    return newFile;
  }

  public void setNewFile(String newFile) {
    this.newFile = newFile;
  }

  public String getOldFileHighlighted() {
    return oldFileHighlighted;
  }

  public void setOldFileHighlighted(String oldFileHighlighted) {
    this.oldFileHighlighted = oldFileHighlighted;
  }

  public String getNewFileHighlighted() {
    return newFileHighlighted;
  }

  public void setNewFileHighlighted(String newFileHighlighted) {
    this.newFileHighlighted = newFileHighlighted;
  }

}
